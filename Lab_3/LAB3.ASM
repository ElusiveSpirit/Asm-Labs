CSEG segment                                    ; Обзываем любым именем
                                                ; сегмент с которым будем
                                                ; работать.
assume cs:CSEG, ds:CSEG, es:CSEG, ss:CSEG       ; Присваем всем регистрам,
                                                ; отвечающим за адреса
                                                ; сегментов нам сегмент.
org 100h         ; Обязательное для .сом файлов смещение от начала сегмента.
                 ; Там будут прописаны другие команды, о которых вы можете
                 ; прочитать в интернете или услышать у Большакова
Init:     ; Метка начала программы

        ; Считываение и вывод на экран
        call GETCH
        call PUTCH
        call CLRF

        call GETCH
        call PUTCH
        call CLRF

        call GETCH
        call PUTCH
        call CLRF

        mov ah, 04Ch    ; Помещаем в регистр AH число 4Ch
        int 21h         ; Вызов прерывания 21h

; Ожидает нажатия на клавишу
; Возвращает в AL код символа.
;
; Если была нажата одна из специальных клавиш, типа
; ESC, ENTER и др. то в код символа будет в AH
GETCH proc
        mov ah, 10h
        int 16h
        ret
GETCH endp

; Помещает символ на экран на место курсора. Курсор сдвигается.
; На вход подаётся код символа в AL
PUTCH proc
        push ax         ; Сохранить в стеке значение регистра AX
                        ; Важно! Необходимо обязательно выравнять стек
                        ; командой pop!!!
        mov ah, 0Eh
        int 10h
        pop ax          ; Вытаскиваем из стека последнее значение и заносим
                        ; в AX
        ret
PUTCH endp

; Переход на следующую строку с помощью вывода двух спец. символов на экран
CLRF proc
          ; Про эти два символа можете где-нибудь почитать
        mov al, 0Dh
        call PUTCH
        mov al, 0Ah
        call PUTCH
        ret
CLRF endp

CSEG ends
end Init