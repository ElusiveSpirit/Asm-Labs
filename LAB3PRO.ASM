CSEG segment                                    ; Обзываем любым именем
                                                ; сегмент с которым будем
                                                ; работать.
assume cs:CSEG, ds:CSEG, es:CSEG, ss:CSEG       ; Присваем всем регистрам,
                                                ; отвечающим за адреса
                                                ; сегментов нам сегмент.
org 100h         ; Обязательное для .сом файлов смещение от начала сегмента.
                 ; Там будут прописаны другие команды, о которых вы можете
                 ; прочитать в интернете или услышать у Большакова
Init:     ; Метка начала программы

        mov dx, offset Message    ; В регистр DX заносим адрес
                                  ; Если убрать слово offset
                                  ; то в DX будет первая буква - "М"
        call WRITE_LINE

Main_Loop:            ; Метка
        call GETCH

        cmp al, '*'   ; Сверяем введённый знак с *
        jz End_Prog   ; Если верно, то переход на метку End

        call PUTCH
        call CLRF

        jmp Main_Loop ; Прыжок на метку
                      ; аналог goto

End_Prog:               ; Метка
        mov ah, 04Ch    ; Помещаем в регистр AH число 4Ch
        int 21h         ; Вызов прерывания 21h


Message db 'Маняхин', 13, 10, 'Константин', 13, 10, 'Александрович', 10, 13, '------------------', 10, 13, '$'

; Мне лень всё это писать по много раз
; В этом файле лежат основные процедуры
include PROCEDUR.asm

CSEG ends
end Init